
/**
 * Datatype to return to the GUI for drawing the clusters and sightings
 * 
 * @author JacobRickman
 *
 */
public class Oval {
	public int midX;
	public int midY;
	public int width;
	public int height;
	
	public Oval(int x, int y, int w, int h)
	{
		midX = x;
		midY = y;
		width = w;
		height = h;
	}
	
}
